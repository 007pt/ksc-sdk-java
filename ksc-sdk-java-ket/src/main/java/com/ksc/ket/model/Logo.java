package com.ksc.ket.model;

public class Logo {
	private String pic;
	private String usage_scenario;
	private Integer short_side;
	private Integer disable_scale;
	private Integer offsetX;
	private Integer offsetY;

	public String getPic() {
		return pic;
	}

	public void setPic(String pic) {
		this.pic = pic;
	}

	public String getUsage_scenario() {
		return usage_scenario;
	}

	public void setUsage_scenario(String usage_scenario) {
		this.usage_scenario = usage_scenario;
	}

	public Integer getShort_side() {
		return short_side;
	}

	public void setShort_side(Integer short_side) {
		this.short_side = short_side;
	}

	public Integer getOffsetX() {
		return offsetX;
	}

	public void setOffsetX(Integer offsetX) {
		this.offsetX = offsetX;
	}

	public Integer getOffsetY() {
		return offsetY;
	}

	public void setOffsetY(Integer offsetY) {
		this.offsetY = offsetY;
	}

	public Integer getDisable_scale() {
		return disable_scale;
	}

	public void setDisable_scale(Integer disable_scale) {
		this.disable_scale = disable_scale;
	}

}
